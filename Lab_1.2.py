a = int(input("Введіть довжину сторони a: "))
b = int(input("Введіть довжину сторони b: "))
c = int(input("Введіть довжину сторони c: "))

if a <= 0 or b <= 0 or c <= 0:
    print("Сторони трикутника повинні бути більше 0")

if a + b > c and a + c > b and b + c > a:
    if a == b == c:
        print(1)  # - рівносторонній
    elif a == b or a == c or b == c:
        print(2)  # - рівнобедрений
    else:
        print(3)  # - різносторонній
